c.execute("UPDATE job_listings SET state='CA' WHERE state='California' AND is_active=1")
c.execute("UPDATE job_listings SET state='CA' WHERE (state='nan' OR state='' OR state IS NULL) AND is_active=1")
conn.commit()

# Get all ungeocoded
c.execute('SELECT DISTINCT city FROM job_listings WHERE is_active=1 AND latitude IS NULL')
cities = [r[0] for r in c.fetchall()]
print(f'Ungeocoded unique cities: {len(cities)}')

updated = 0
missed = []

for city in cities:
    if not city or str(city).lower() == 'nan':
        continue
    key = str(city).strip().lower()
    # Normalize accented characters for lookup
    key = key.replace('\xf1', 'n').replace('\xe9', 'e').replace('\xed', 'i').replace('\xf3', 'o').replace('\xfa', 'u')
    if key in ALL_CA_COORDS:
        lat, lon = ALL_CA_COORDS[key]
        c.execute('UPDATE job_listings SET latitude=?, longitude=? WHERE city=? AND is_active=1',
                  (lat, lon, city))
        updated += 1
    else:
        missed.append(city)

conn.commit()

c.execute('SELECT COUNT(*) FROM job_listings WHERE is_active=1 AND latitude IS NOT NULL')
geocoded = c.fetchone()[0]
c.execute('SELECT COUNT(*) FROM job_listings WHERE is_active=1')
total = c.fetchone()[0]
conn.close()

print(f'Geocoded this run:  {updated} cities')
print(f'Total with coords:  {geocoded} / {total} jobs')
if missed:
    print(f'Still missing ({len(missed)}): {sorted(missed)}')
